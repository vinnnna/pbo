fun main() {
    var a = 99

    if (a <= 50)
    {
        println("Failed")
    }

    else if (a <= 65)
    {
        println("Grade C")
    }

    else if (a <= 70)
    {
        println("Grade B-")
    }

    else if (a <= 80)
    {
        println("Grade B")
    }

    else if (a <= 90)
    {
        println("Grade A-")
    }

    else
    {
        println("Grade A")
    }
}
